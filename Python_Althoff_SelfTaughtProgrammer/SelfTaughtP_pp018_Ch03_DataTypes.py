
# str (string) objects are a sequence of one or more characters
print("Hello")
print('Goodbye')

# int (integer) objects are multiples of one/whole numbers
print("24")
print('-354')

# float (floating-point) objects are decimal numbers
print("3.14")
print('6.11')

# bool (boolean) objects contain 'True' or 'False' values
myBool = True
yourBool = False
print(myBool)
print(yourBool)

# NoneType objects contain a value of "None", representing the absense of value
myNoneType = None
print(myNoneType)

