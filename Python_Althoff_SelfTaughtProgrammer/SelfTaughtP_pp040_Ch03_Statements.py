
# Simple statements span one line, compound statements typically span more.

# Statement syntax:

Goyle = "Goyle is a moronic oaf." # <-- Simple statement

if Goyle == "Goyle is a moronic oaf.": # <-- Compound Statement Clause #1 Header
    print(Goyle) #                       <-- Compound Statement Clause #1 Suite
